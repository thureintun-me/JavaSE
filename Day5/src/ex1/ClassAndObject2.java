package ex1;

public class ClassAndObject2 {

	public static void main(String[] args) {

		String str1 = new String("I am object instance of a String class");
		System.out.println("value of str 1 : " + str1);
		
		
		String str2 = "I am object instance of String class";
		System.out.println("value of str2 :" + str2);
		
		Integer int1 = new Integer(7);
		System.out.println("value of int 1 : " + int1);
		
		Integer int2 = new Integer(77);
		System.out.println("value of int2 : " + int2);
		
	}

}
